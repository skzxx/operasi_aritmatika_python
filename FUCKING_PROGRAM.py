#Operasi aritmatika

#Penjumlahan

Operator_Aritmatika_A = 10;
Operator_Aritmatika_B = 3;
Operator_Hasil_Kalkulasi = (Operator_Aritmatika_A + Operator_Aritmatika_B);
print("1.Penjumlahan dari ", Operator_Aritmatika_A, "+", Operator_Aritmatika_B, "=", Operator_Hasil_Kalkulasi);

#Pengurangan

Operator_Hasil_Kalkulasi = (Operator_Aritmatika_A - Operator_Aritmatika_B);
print("2.Jumlah dari ", Operator_Aritmatika_A, "-", Operator_Aritmatika_B, "=", Operator_Hasil_Kalkulasi);

#Perkalian

Operator_Hasil_Kalkulasi = (Operator_Aritmatika_A * Operator_Aritmatika_B);
print("3.Jumlah dari ", Operator_Aritmatika_A, "*", Operator_Aritmatika_B, "=", Operator_Hasil_Kalkulasi);

#Pembagian

Operator_Hasil_Kalkulasi = (Operator_Aritmatika_A / Operator_Aritmatika_B);
print("2.Jumlah dari ", Operator_Aritmatika_A, "/", Operator_Aritmatika_B, "=", Operator_Hasil_Kalkulasi);

#eksponen

Operator_Hasil_Kalkulasi = (Operator_Aritmatika_A ** Operator_Aritmatika_B);
print("4.Jumlah dari ", Operator_Aritmatika_A, "**", Operator_Aritmatika_B, "=", Operator_Hasil_Kalkulasi);

#Modulus

Operator_Hasil_Kalkulasi = (Operator_Aritmatika_A % Operator_Aritmatika_B);
print("5.Jumlah dari ", Operator_Aritmatika_A, "%", Operator_Aritmatika_B, "=", Operator_Hasil_Kalkulasi);

